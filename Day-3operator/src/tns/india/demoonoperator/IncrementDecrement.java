package tns.india.demoonoperator;

public class IncrementDecrement {
	public static void main(String[] args) {
		//increment
		int a=13,b=5;
		a++;
		int y=a;
		++b;
		System.out.println(b);
		System.out.println(y);
		//decrement
		int x=4,z=11;
		--x;
		--z;
		int p=++z;
		System.out.println(x);
		System.out.println(p);
		

	}

}


