package org.tnsindia.collectiondemo;

public class Employee {
	//private data members
	private int age;
	private String name;
	//getters and setters method
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	

}
